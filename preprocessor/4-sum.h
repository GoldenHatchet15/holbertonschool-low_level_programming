#ifndef SUM_H
#define SUM_H

/**
 * SUM - computes the sum of x and y.
 * @x: The first number.
 * @y: The second number.
 *
 * Description: This macro simply adds the values of x and y.
 * Parentheses around the arguments ensure the correct order of evaluation.
 */
#define SUM(x, y) ((x) + (y))

#endif /* SUM_H */
